package in.oops;

public class While {

	public static void main(String[] args) {
		
		 int i=10;
         do{
              System.out.println(i);
              i--;
         }while(i>1);
         //exit level checking condition 
    
         int j=1;
         while(j<10){
              System.out.println(j);
              j++;
         }
         //entry level condition checking
    }
	}
	
